#include <stdio.h>
#include <stdlib.h>
#include <assert.h>
#include <string.h>
#include <sys/time.h>
#include <sys/types.h>
#include <time.h>

#include <netinet/in.h>
#include <netinet/in_systm.h>
#include <netinet/tcp.h>
#include <netinet/ip.h>
#include <netinet/ip_icmp.h>
#include <arpa/inet.h>
#include <sys/socket.h>
#include "dagformat.h"
#include "libtrace.h"


int main(int argc, char *argv[]) {
        char *uri = "erf:./traces/100_packets.erf";
	struct libtrace_t *trace;
	
	trace = trace_create(uri);
	
	if (trace) {
		printf("success: can open erf traces\n");	
		trace_destroy(trace);
		return 0;
	}
	printf("failure: cannot open erf traces\n");
	trace_perror("trace_create");
	return -1;
}
